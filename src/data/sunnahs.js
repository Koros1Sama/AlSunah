// Helper: get all sunnahs for a category
export function getSunnahsByCategory(categoryId) {
  const cat = categories.find(c => c.id === categoryId);
  if (!cat) return [];
  const result = [];
  for (const unitId of cat.units) {
    const unit = units[unitId];
    if (unit) {
      for (const lessonId of unit.lessons) {
        if (sunnahs[lessonId]) result.push(sunnahs[lessonId]);
      }
    }
  }
  return result;
}

// Helper: get all evidence items
export function getAllEvidence() {
  return Object.values(sunnahs).map(s => ({
    ...s.evidence,
    sunnahName: s.name,
    sunnahId: s.id,
    categoryId: units[s.unitId]?.categoryId,
  }));
}

// Helper: get evidence by category
export function getEvidenceByCategory(categoryId) {
  return getAllEvidence().filter(e => e.categoryId === categoryId);
}

// Helper: total counts
export function getTotalLessons() {
  return Object.keys(sunnahs).length;
}

export function getTotalUnits() {
  return Object.keys(units).length;
}
